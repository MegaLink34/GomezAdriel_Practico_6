    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String dni = textFieldDNI.getText();
                
        if (!dni.equals("")){
            
            for(Map.Entry<String, Cliente> e: directorio.entrySet()){
                
                if (e.getValue().getDni().equals(dni)) {
                    
                    textFieldNombre.setText(e.getValue().getNombre());
                    textFieldApellido.setText(e.getValue().getApellido());
                    textFieldDomicilio.setText(e.getValue().getDomicilio());
                    textFieldCiudad.setText(e.getValue().getCiudad());
                    textFieldTelefono.setText(e.getKey());
                    
                }
            }
        } else {
            
            JOptionPane.showMessageDialog(null, "Ingrese un dni");
            
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


