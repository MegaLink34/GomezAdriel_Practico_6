    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        // TODO add your handling code here:     
        
        String dni = textFieldDNI.getText();
                
        if (!dni.equals("")){
            String dniEncontrado = "";
            for(Map.Entry<String, Cliente> e: directorio.entrySet()){
                
                if (e.getValue().getDni().equals(dni)) {
                    
                    dniEncontrado = e.getKey();
                    JOptionPane.showMessageDialog(null, "Dni encontrado.");
                    
                }
            }
            if(dniEncontrado.equals("")){
                JOptionPane.showMessageDialog(null, "Dni no encontrado.");
            }else{
                MenuPrincipal.getDirectorio().borrarCliente(dniEncontrado);
                JOptionPane.showMessageDialog(null, "Cliente eliminado.");
            }
        } else {
            
            JOptionPane.showMessageDialog(null, "Ingrese un dni");
            
        }
    }//GEN-LAST:event_btnBorrarActionPerformed

