    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nombre = textFieldNombre.getText();
        String apellido = textFieldApellido.getText();
        String dni = textFieldDNI.getText();
        String domicilio = textFieldDomicilio.getText();
        String ciudad = textFieldCiudad.getText();
        String telefono = textFieldTelefono.getText();
        
        Cliente c = new Cliente(nombre, apellido, dni, domicilio, ciudad);
        
        if(directorio.containsKey(telefono)) {
            JOptionPane.showMessageDialog(null, "Este numero de telefono ya est√° en el directorio.");
        } else {
            if ("".equals(apellido) || "".equals(dni) || "".equals(ciudad) || "".equals(nombre) || "".equals(domicilio) || "".equals(telefono)){
                JOptionPane.showMessageDialog(null, "Faltan datos, complete todos los campos antes de clickear guardar.");
            } else {
                MenuPrincipal.getDirectorio().agregarCliente(telefono, nombre, apellido, dni, ciudad, domicilio);
                JOptionPane.showMessageDialog(null, "Cliente ingresado correctamente.");
            }
            
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

